    <p class="Normal">Tính năng giải trí trên Zenfone thế hệ mới được chú trọng bằng việc  nâng cấp camera sau lên 13 megapixel, ống kính f/2.0 đi cùng đèn flash  kép Real Tone. Theo xu hướng smartphone "selfie", máy ảnh trước có độ  phân giải 5 megapixel với ống kính góc rộng.</p>
    <p class="Normal">Có giá bán tốt nhưng Asus cũng trang bị khá nhiều tính năng mở rộng cho  Zenfone 2. Máy dùng viên pin dung lượng 3.000 mAh với tính năng sạc  nhanh cho phép nạp từ 0% lên 60% chỉ mất 39 phút. Sản phẩm còn sở hữu 2  khe cắm SIM và kết nối mạng 4G tốc độ cao.</p>
    <p class="Normal">Theo công bố, Zenfone 2 bán ra sẽ chạy Android 5.0 Lollipop với giao  diện ZenUI hoàn toàn mới. Thiết bị hỗ trợ kết nối Wi-Fi ac, Wi-FI  Direct, Bluetooth 4.0 và NFC. Các thông số có thể thay đổi theo từng  phiên bản, tại các thị trường khác nhau.</p>
