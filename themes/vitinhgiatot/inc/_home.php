    <p><strong>Asus ra Zenfone 2 với RAM 4 GB, giá từ 4 triệu đồng  </strong></p>
    <p>Có giá bán từ 199 USD,  Zenfone 2 hứa hẹn tiếp tục khuấy động phân khúc smartphone giá rẻ nhờ  những cải tiến rất đáng giá từ ngoại hình, camera đến hiệu năng.</p>
    <p class="Normal">Bản nâng cấp cho dòng Zenfone là một trong những loạt thiết bị được  mong chờ nhất tại CES 2015. Đúng như thông tin Asus hé lộ, Zenfone 2  được hãng nâng cấp đáng kể cả về thiết kế và cấu hình nhưng vẫn tạo lợi  thế cạnh tranh nhờ giá bán hấp dẫn.</p>
