    <p class="Normal">Sản phẩm của nhà sản xuất Đài Loan có mặt lưng cong, thiết kế các đường  phay xước như kim loại. Đây cũng là điểm nhấn trên Zenfone 2 khi máy  đưa cụm phím âm lượng từ cạnh bên ra phía sau, cách làm khá giống với  một số mẫu smartphone của LG.</p>
    <p class="Normal">Mặt trước Zenfone 2 là màn hình IPS 5,5 inch độ phân giải Full HD, cho  mật độ điểm ảnh khoảng 403 ppi. So với thế hệ đầu, cải tiến đáng kể của  sản phẩm mới không chỉ đến từ chất lượng hiển thị mà còn là phần viền  mỏng chỉ 3,9 mm giúp màn hình chiếm 72% diện tích mặt trước.</p>
    <p class="Normal">Về hiệu năng xử lý, Zenfone 2 được nâng cấp với chip 64-bit Intel Atom  Z3580 tốc độ 2,3 GHz, RAM lên đến 4 GB với bộ nhớ trong tùy chọn 16 GB,  32 GB, 64 GB và có thể mở rộng thông qua thẻ nhớ. Asus cũng phát hành  phiên bản dùng RAM 2 GB, bộ xử lý xung nhịp thấp hơn hứa hẹn sẽ có giá  bán tốt.</p>
